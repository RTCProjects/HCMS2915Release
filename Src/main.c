/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{
	
  /* USER CODE BEGIN 5 */
	
	HCMS_Init();
	
	System_Init();
	Graphic_Init();
  /* Infinite loop */
	
	  /* Check and handle if the system was resumed from StandBy mode */ 
  if(__HAL_PWR_GET_FLAG(PWR_FLAG_SB) != RESET)
  {
    /* Clear Standby flag */
    __HAL_PWR_CLEAR_FLAG(PWR_FLAG_SB); 
  }
	
  for(;;)
  {
		System_Process();
		Graphic_Process();
		
		osDelay(1);
  }
  /* USER CODE END 5 */ 
}

